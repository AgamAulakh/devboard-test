/**
* @brief Function implementing the motorPWM thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_motorPWMTask */
void motorPWMTask(void const * argument)
{
  /* USER CODE BEGIN motorPWMTask */
  // extra 3.3V
  HAL_GPIO_WritePin(VENT_1_GPIO_Port, VENT_1_Pin, 1);
  HAL_GPIO_WritePin(VENT_2_GPIO_Port, VENT_2_Pin, 1);
  HAL_GPIO_WritePin(VENT_3_GPIO_Port, VENT_3_Pin, 1);

  // pwm start, NOTE:
  // CCR1 = 25 for 0 degrees
  // CCR1 = 58 for 90 degrees
  // CCR1 = 92 for 180 degrees
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  htim2.Instance->CCR1 = 25;

  uint32_t prevWakeTime = osKernelSysTick();
  uint16_t counter = 0;
  for (;;)
  {
    if (HAL_GPIO_ReadPin(PB_BLUE_GPIO_Port, PB_BLUE_Pin) == 1) {
      // handle push button press
      htim2.Instance->CCR1 = 58;
      counter = 5;
    }
    else if (counter == 5) {
      // reset to original CCR1 when button released
      htim2.Instance->CCR1 = 25;
      counter = 0;
    }

    switch (counter)
    {
      case 0:
        HAL_GPIO_TogglePin(LD3_GPIO_Port, LD3_Pin);
        counter = 1;
        break;

      case 1:
        HAL_GPIO_TogglePin(LD4_GPIO_Port, LD4_Pin);
        counter = 2;
        break;

      case 2:
        HAL_GPIO_TogglePin(LD6_GPIO_Port, LD6_Pin);
        counter = 3;
        break;

      case 3:
        HAL_GPIO_TogglePin(LD5_GPIO_Port, LD5_Pin);
        counter = 0;
        break;

      case 5:
        // case for moving servo arm, all LEDs on to verify
        HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, 1);
        HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, 1);
        HAL_GPIO_WritePin(LD5_GPIO_Port, LD5_Pin, 1);
        HAL_GPIO_WritePin(LD6_GPIO_Port, LD6_Pin, 1);
        counter = 5;
        break;

      default:
        break;
    }

    osDelayUntil(&prevWakeTime, 250);
  }
  /* USER CODE END motorPWMTask */
}

